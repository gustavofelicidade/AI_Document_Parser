def extract_info(content, patterns):
    extracted_info = {}
    for key, pattern in patterns.items():
        match = re.search(pattern, content)
        if match:
            extracted_info[key] = match.group(1).strip()
        else:
            extracted_info[key] = None
    return extracted_info

